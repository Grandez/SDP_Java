package com.jgg.sdp.web.json.modulo;

public class Comment {

	private int blancos;	
	private int comentarios;
	private int bloques;
	private int decoradores;
	
	public int getBlancos() {
		return blancos;
	}
	public void setBlancos(int blancos) {
		this.blancos = blancos;
	}
	public int getComentarios() {
		return comentarios;
	}
	public void setComentarios(int comentarios) {
		this.comentarios = comentarios;
	}
	public int getBloques() {
		return bloques;
	}
	public void setBloques(int bloques) {
		this.bloques = bloques;
	}
	public int getDecoradores() {
		return decoradores;
	}
	public void setDecoradores(int decoradores) {
		this.decoradores = decoradores;
	}
	
	
}
